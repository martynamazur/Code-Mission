package com.example.nauka;


public class MyItem {
    private String title;
    private int imageRes;

    public MyItem(String title, int imageRes) {
        this.title = title;
        this.imageRes = imageRes;
    }

    public String getTitle() {
        return title;
    }

    public int getImageRes() {
        return imageRes;
    }

    public String getText() {
        return title;
    }


}
